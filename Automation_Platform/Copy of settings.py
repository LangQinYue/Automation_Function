def log_date(name):
    day = datetime.date.today()
    date_path = os.path.dirname(os.path.dirname(__file__)) + "/log/" + str(day)
    if os.path.exists(date_path):
        pass
    else:
        os.makedirs(date_path)
    return date_path + "/%s" % name


